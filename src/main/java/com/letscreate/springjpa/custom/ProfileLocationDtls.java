package com.letscreate.springjpa.custom;

public class ProfileLocationDtls {
	String profileCountryName;
	String profileStateName;
	String profileCityName;
	public String getProfileCountryName() {
		return profileCountryName;
	}
	public void setProfileCountryName(String profileCountryName) {
		this.profileCountryName = profileCountryName;
	}
	public String getProfileStateName() {
		return profileStateName;
	}
	public void setProfileStateName(String profileStateName) {
		this.profileStateName = profileStateName;
	}
	public String getProfileCityName() {
		return profileCityName;
	}
	public void setProfileCityName(String profileCityName) {
		this.profileCityName = profileCityName;
	}
	
	
}
